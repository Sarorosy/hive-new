const stripLeadingSlash = (path = "") => path.replace(/^\/+/, "");

export const assetPath = (path) => {
  const normalizedBase = import.meta.env.BASE_URL?.endsWith("/")
    ? import.meta.env.BASE_URL
    : `${import.meta.env.BASE_URL || ""}/`;

  return `${normalizedBase}${stripLeadingSlash(path)}`;
};

